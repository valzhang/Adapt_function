% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

value = get(handles.listbox1, 'Value');
AllCode = get(handles.listbox1, 'String');
code = AllCode(value);
value = get(handles.listbox2, 'Value');
AllCycle = get(handles.listbox2, 'String');
if (value == 1)
    cycle = -1;
else
    cycle = AllCycle(value);
    cycle = str2num(cell2mat(cycle));
end
start_date = get(handles.edit1, 'String');
end_date = get(handles.edit2, 'String');
start_date = [start_date ' 09:00:00'];
end_date = [end_date ' 15:15:00'];
[outStat, dailydetail, monthdetail, allsignal, signalSeries]=XY_30MinBreakOut_2min_New(cell2mat(code), cycle, start_date, end_date);
if cell2mat(outStat(5, 2)) < 0
    errordlg('选定时间内没有该期货品种的交易数据！','错误信息','on') ;
    set(handles.edit5, 'String', '0.0');
    set(handles.edit6, 'String', 0);
    set(handles.edit7, 'String', 0);
    set(handles.edit8, 'String', '0.0');
    set(handles.edit9, 'String', '0.0');
    set(handles.edit10, 'String', '0.0');
    set(handles.edit11, 'String', '0.0');
    cla(handles.axes1);
else
    set(handles.edit5, 'String', cell2mat(outStat(4,2)));
    set(handles.edit6, 'String', cell2mat(outStat(5,2)));
    set(handles.edit7, 'String', cell2mat(outStat(9,2)));
    set(handles.edit8, 'String', cell2mat(outStat(13,2)));
    set(handles.edit9, 'String', cell2mat(outStat(12,2)));
    set(handles.edit10, 'String', cell2mat(outStat(27,2)));
    set(handles.edit11, 'String', cell2mat(outStat(6,2)));
    
    [row col] = size(monthdetail);
    month_data = cell2mat(monthdetail(2:row, 2:col));
    axes(handles.axes1)    
    bar(month_data(:,2), 0.5);
    len = length(month_data);
    gap = len/4;
    set(handles.axes1,'XTick',[fix(1:gap:len) len]);
    set(handles.axes1,'XTickLabel',monthdetail([fix(2:gap:row) row], 1));
    
        set(handles.axes1,'xlim', [0 len+1]);    
        xtl=get(handles.axes1,'XTickLabel');
        xt=get(handles.axes1,'XTick');
        yt=get(handles.axes1,'YTick');
         xtextp=xt ;
         ytextp=(yt(1)-0.2*(yt(2)-yt(1)))*ones(1,length(xt));
         text(xtextp-((xt(2)-xt(1))/2),ytextp-max(month_data(:,2))/20,xtl,'HorizontalAlignment','left','rotation',25,'fontsize', 10) ;
         set(handles.axes1,'xticklabel','');
         
    [row col] = size(dailydetail);
    day_data = cell2mat(dailydetail(2:row, 2:col));
    axes(handles.axes2)
    plot(1:length(day_data), cumsum(day_data(:,2)), '-r*');   
    
    
    %dailydetail(2:row, 1)
   % size(dailydetail(2:row, 1))
    set(handles.axes2,'xlim', [0 length(day_data)+1]);
    len = length(day_data);
    gap = len/8;
    set(handles.axes2,'XTick',[fix(1:gap:len) len]);
    set(handles.axes2,'XTickLabel',dailydetail([fix(2:gap:row) row], 1));
    
    xtl=get(handles.axes2,'XTickLabel');
        xt=get(handles.axes2,'XTick');
        yt=get(handles.axes2,'YTick');
         xtextp=xt ;
         ytextp=(yt(1)-0.2*(yt(2)-yt(1)))*ones(1,length(xt));
         
         text(xtextp-gap/2,ytextp-max(cumsum(day_data(:,2))/15),xtl,'HorizontalAlignment','left','rotation',25,'fontsize', 10) ;
         set(handles.axes2,'xticklabel','');
         
    %set(handles.axes2,'XTickLabel',dailydetail(2:row, 1));
end

